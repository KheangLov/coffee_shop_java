    private void btnImportMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnImportMouseClicked
        // TODO add your handling code here:
        double qty = Double.valueOf(txtQty.getText().trim());
        double price = Double.valueOf(txtPrice.getText().trim());
        Date currentDate = new Date();

        DateFormat dtf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            
        int supId = AppHelper.getId(
            String.valueOf(cbSuplliers.getSelectedItem()), 
            "id", 
            "suppliers", 
            "name"
        );
        myImport.setCreatedDate(dtf.format(currentDate));
        myImport.setUpdatedDate(dtf.format(currentDate));
        myImport.setUserId(uId);
        myImport.setSupplierId(supId);
        myImport.insert();
        
        if(myImport.isInserted()) {
            myImpDetail.setImportId(myImport.getId());
            myImpDetail.setStockId(sId);
            myImpDetail.setQty(qty);
            myImpDetail.setPrice(price);
            myImpDetail.insert();
            
            String sql = "SELECT qty FROM stocks "
                + "WHERE id = ?";
            ResultSet rs = AppHelper.selectQuery(sql, sId);
            try {
                if (rs.next())
                    myStock.setQty(rs.getDouble("qty"));
            } catch (SQLException ex) {
                Logger.getLogger(ImportForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            Double newQty = myStock.getQty() + qty;
            myStock.update(sId, "qty", newQty);

            JOptionPane.showMessageDialog(null, myImpDetail.getMessage());
            this.dispose();
        }
    }//GEN-LAST:event_btnImportMouseClicked

