    private void txtAddConPassFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtAddConPassFocusLost
        // TODO add your handling code here:
        int min = 8;
        if(!txtAddConPass.getText().trim().equals("")) {
            if(!AppHelper.isMatchLength("min", min, txtAddConPass.getText().trim().length())) {
                AppHelper.errorMessage("password", min);
                txtAddConPass.requestFocus();
            }
            if(!String.valueOf(txtAddPassword.getPassword()).equals(String.valueOf(txtAddConPass.getPassword()))) {
                JOptionPane.showMessageDialog(null, "Password and Confirm Password does not match!");
                txtAddConPass.setText("");
                txtAddPassword.setText("");
                txtAddPassword.requestFocus();
            }
        }
    }//GEN-LAST:event_txtAddConPassFocusLost

