    private void btnAddUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddUserMouseClicked
        // TODO add your handling code here:
        PreparedStatement st;
        ResultSet rs;
        String firstname = txtAddFname.getText().trim();
        String lastname = txtAddLname.getText().trim();
        String fullname = firstname + lastname;
        String email = txtAddEmail.getText().trim();
        String password = String.valueOf(txtAddPassword.getPassword()).trim();
        String conPassword = String.valueOf(txtAddConPass.getPassword()).trim();
        String status = String.valueOf(cbAddStatus.getSelectedItem()).trim();
        String gender = String.valueOf(cbAddGender.getSelectedItem()).trim();
        String role = String.valueOf(cbAddRole.getSelectedItem()).trim();
        int userType = 0;
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        String date = dtf.format(now);
        if(firstname.equals("") || lastname.equals("") || email.equals("") || password.equals("") || conPassword.equals("")) {
            AppHelper.fieldRequiredMsg();
            txtAddFname.requestFocus();
        } else {
            Boolean checkExist = AppHelper.isExist("users", "fullname", fullname);
            if(checkExist.equals(true)) {
                AppHelper.existMsg();
                txtAddPassword.setText("");
                txtAddEmail.setText("");
                txtAddFname.setText("");
                txtAddLname.setText("");
                txtAddConPass.setText("");
                cbAddRole.setSelectedIndex(0);
                cbAddGender.setSelectedIndex(0);
                cbAddStatus.setSelectedIndex(0);
                txtAddFname.requestFocus();
            } else {
                int roleId = AppHelper.getId(role, "id", "roles", "name");
                String hashedPass = PasswordEncryption.MD5(password);
                myUser.setFirstname(firstname);
                myUser.setLastname(lastname);
                myUser.setFullname(fullname);
                myUser.setEmail(email);
                myUser.setPassword(hashedPass);
                myUser.setStatus(status);
                myUser.setGender(gender);
                myUser.setUser_type(userType);
                myUser.setRole_id(roleId);
                myUser.setCreated_date(date);
                myUser.setUpdated_date(date);
                myUser.insert();
                action = true;
                txtAddPassword.setText("");
                txtAddEmail.setText("");
                txtAddFname.setText("");
                txtAddLname.setText("");
                txtAddConPass.setText("");
                cbAddRole.setSelectedIndex(0);
                cbAddGender.setSelectedIndex(0);
                cbAddStatus.setSelectedIndex(0);
                txtAddFname.requestFocus();
            }
        }
    }//GEN-LAST:event_btnAddUserMouseClicked

